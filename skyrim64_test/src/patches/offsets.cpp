	static uint32_t CKVersion = 0;
	std::unordered_map<uint64_t, uintptr_t> AddressMap;

	uintptr_t Resolve(uint32_t RelOffset, uint32_t Version)
	{
		return AddressMap.at(OFFSET_ENTRY_KEY(RelOffset, Version));
	}

	bool CanResolve(uint32_t RelOffset, uint32_t Version)
	{
		return Resolve(RelOffset, Version) != 0;
	}

	bool IsCKVersion16438(void)
	{
		return CKVersion == 16438;
	}

	bool IsCKVersion1573(void)
	{
		return CKVersion == 1573;
	}

	bool IsCKVersion16438OrNewer(void)
	{
		return CKVersion >= 16438;
	}

	bool IsCKVersion1573OrNewer(void)
	{
		return CKVersion >= 1573;
	}

	void BuildTableForCKSSEVersion(uint32_t Version)
	{
		if (Version >= 16438)
			BuildTable(EntryListCK16438, Version == 16438);

		if (Version >= 1573)
			BuildTable(EntryListCK1573, Version == 1573);

		if (Version >= 1530)
			BuildTable(EntryListCK1530, Version == 1530);

		CKVersion = Version;
	}

